/**
 * Check if curr user has workspaces, if yes - redirect to first one
 */
Route::filter('got_workspaces', function()
{
	$user_workspaces = DB::table('user2group')->
		select('groups.hash')->
		where('user_id', '=', Session::get('user_id'))->
		join('groups', 'groups.id', '=', 'user2group.group_id')->
		get();
	if (!empty($user_workspaces)) {
		return Redirect::route('workspace', ['hash' => $user_workspaces[0]->hash]);
	}
});

